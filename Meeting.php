<p>Elephant Festival is a festival celebrated in Jaipur city in Rajasthan state in India. It is held on the day of Holi festival, usually in the month of March. The festival features Elephant polo and Elephant Dance. The Elephant Festival begins with a beautiful procession of bedecked elephants, camels, horses and folk dancers. The owners proudly embellish their elephants with vibrant colors, jhools (saddle cloth) and heavy jewellery. It is quite a treat to see female elephants wearing anklets, which tinkle as they walk. One can see people sprinkling 'gulaal' (colored powder) perched on top of the elephant. The most beautifully decorated elephant is awarded. Elephant polo, Elephant race, the tug-of-war between Elephant and 19 men and women are the featured events of the festival. The elephants are also outfitted in ear danglers and brocade scarves to embellish their ears and necks. The Mahouts, who are known as the caretakers of the elephants, decorate the foreheads of the animals with head-plates as well as garnish their tusks with gold, silver bracelets and rings. Other events that take place during the festival include a polo match and a tug of war contest, where the strongest elephant is chosen to compete against a group of ten people.

Although the festival is primarily based on elephants, other animals such as camels and horses also participate in the elephant festival.
