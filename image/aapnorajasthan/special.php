<p>"Teej" refers to the "third" day that falls every month after the new moon (Amavasya), and the third day after the full moon night of every month.
Accordingly, Teej can refer to any festival falling on such a day but when speaking of Teej, the festivals of Haryali Teej, Kajari Teej and Hartalika Teej are classed together. The other forms of Teej are separate festivals: such as Akshaya Tritiya).[2] Awra Teej is celebrated in the lunar month of Vaisakh in parts of Madhya Pradesh and Chhattisgarh: Awra Teej is not associated with the trio of the monsoon festivals of Teej. The Jhulan Utsav of Vrindaban is also an independent festival.

