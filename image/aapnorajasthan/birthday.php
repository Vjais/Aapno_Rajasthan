<p>Dussehra Festival is very popular and famous festival of Rajasthan. It is the one of the big festivals celebrated in most parts of India. Specially this festival celebrated in kota where people celebrated it most. Dussehra Festival and fair is held in Kota, each year on Hindu month Ashvin (Sept-Oct). Dussehra marks the victory of Ram over the devil king Ravan. It was good over the evil. 

On this occasion people organised colorful procession of lord Rama. This procession goes to the main market with hundreds of people. A major attraction for the children in this procession are the Vanar Senas or the monkey forces. At last this procession goes to the fair of Dussehra. Fairs are generally held on this festival with lots to eat, buy and enjoy.

This day people is also known as Vijayadashmi. On this occasion in the big ground Ravana along with his brother Kumbhkarana and his son Meghnath, made of paper & bamboo and stuffed with crackers are set alight. people enjoy this festival after the effigy of the Ravana is burnt</p>
